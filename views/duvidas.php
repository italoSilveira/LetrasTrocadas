			Antes de enviar um livro, verifique as melhores opções de envio, os Correios disponibilizam uma forma chamada "Impresso Normal", como entrega de 5 a 7 dias úteis, os valores são os mesmos para qualquer origem e destino dentro do Brasil.<br>
