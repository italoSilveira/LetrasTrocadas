			OBS: valores estimados para livros enviados na modalidade "Impresso Normal" e registrados através do 'registro módico' (modalidade de registro com 50% de desconto, válida especialmente para livros). 
<br><br>
O Registro Módico é uma modalidade de registro dos Correios que é própria para livros, e que custa metade do registro convencional. Eventualmente algumas agências dos correios desconhecem esse serviço. Se isso acontecer, imprima e leve para o gerente dessa agência a página deste link, e faça valer o seu direito de utilizar esse serviço, que foi criado justamente para facilitar o acesso aos livros: 
http://www.correios.com.br/produtosaz/produto.cfm...
<br><br>
O Impresso Normal é uma modalidade de postagem dos Correios que é própria para material impresso (inclusive livros), e que custa menos do que a modalidade Encomenda Normal, que é geralmente a opção utilizada para encomendas de todo o tipo. Além de ser mais barata, outra vantagem é que seu custo não varia geograficamente, o que é especialmente interessante para envios que envolvem localidades fora da região Sudeste, que via Encomenda Normal têm preços muitas vezes proibitivos e via Impresso Normal custam rigorosamente o mesmo do que custariam dentro dessa região. Porém, assim como acontece com o Registro Módico, eventualmente algumas agências dos correios desconhecem esse serviço. Se isso acontecer, aqui estão os seus argumentos.
O link traz abaixo traz a descrição do serviço, confirmando que ele existe e que serve para livros sim. 
